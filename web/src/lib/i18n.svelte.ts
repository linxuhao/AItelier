// ── Translation function ──

/**
 * Translate a key to the current language.
 *
 * @param key - Dot-notation key in the translations dictionary.
 * @returns Translated string, or the key itself if not found.
 *
 * This function reads the module-level `currentLang` $state variable.
 * Svelte 5's compiler instruments the read with `$.get()`, creating a
 * reactive dependency. When `currentLang` changes (via the langStore
 * subscription), all components calling t() automatically re-render.
 */
export function t(key: string): string {
  const lang = currentLang;
  // Try full language code first, then language-only prefix (e.g. "zh" for "zh-CN")
  const map = (translations as Record<string, Record<string, string>>)[lang]
    || (translations as Record<string, Record<string, string>>)[lang.split('-')[0]];
  if (map && map[key]) return map[key];
  // Fallback to English
  const enMap = translations['en'];
  return enMap[key] || key;
}
